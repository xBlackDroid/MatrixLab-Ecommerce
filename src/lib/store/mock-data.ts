type ProductSeed = Omit<
  ProductRow,
  "images" | "tags" | "created_at" | "updated_at" | "min_quantity" | "max_quantity"
> & {
  tags?: string[];
  min_quantity?: number;
  max_quantity?: number;
};

function product(seed: ProductSeed): ProductRow {
  return {
    images: [],
    tags: seed.tags ?? [],
    min_quantity: seed.min_quantity ?? 1,
    max_quantity: seed.max_quantity ?? 999,
    created_at: NOW,
    updated_at: NOW,
    ...seed,
  } as ProductRow;
}

